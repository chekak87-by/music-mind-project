let currentPQuestion = 0;
let scores = { A: 0, B: 0, C: 0, D: 0 };
const pThemeColors = ['#4A90E2', '#E24A4A', '#E24A9B', '#E29B4A', '#4AE2C0'];
let pColorIndex = 0;

function renderPsychoQuiz() {
    const container = document.getElementById('psycho-quiz-container');
    const progress = document.getElementById('psycho-progress');
    const nextBtn = document.getElementById('next-psycho-btn');
    
    container.innerHTML = '';
    progress.innerText = `Вопрос ${currentPQuestion + 1} из ${psychoQuestions.length}`;

    const q = psychoQuestions[currentPQuestion];
    const questionDiv = document.createElement('div');
    questionDiv.className = 'question-card';
    
    let optionsHTML = '';
    q.o.forEach((opt, idx) => {
        optionsHTML += `
            <label class="option-label">
                <input type="radio" name="pq_option" value="${opt.v}">
                ${opt.t}
            </label>
        `;
    });

    questionDiv.innerHTML = `<h4>${currentPQuestion + 1}. ${q.q}</h4><div class="options-group">${optionsHTML}</div>`;
    container.appendChild(questionDiv);

    if (currentPQuestion === psychoQuestions.length - 1) {
        nextBtn.innerText = "Узнать психотип";
    }
}

function nextPsychoQuestion() {
    const selected = document.querySelector(`input[name="pq_option"]:checked`);
    
    if (!selected) {
        alert("Пожалуйста, выбери один из вариантов!");
        return;
    }

    // Записываем балл в нужную категорию
    scores[selected.value]++;

    // Смена цвета темы
    document.documentElement.style.setProperty('--accent-color', pThemeColors[pColorIndex]);
    pColorIndex = (pColorIndex + 1) % pThemeColors.length;

    currentPQuestion++;

    if (currentPQuestion < psychoQuestions.length) {
        renderPsychoQuiz();
    } else {
        showPsychoResult();
    }
}

